## 1. Collect and write manifest JSON

using Dates
using JSON

function write_json_file(path::AbstractString, object)
    ensure_parent_dir(path)
    open(path, "w") do io
        JSON.print(io, object, 2)
        write(io, "\n")
    end
    return path
end

function read_json_file(path::AbstractString)
    return JSON.parsefile(path)
end

function make_release_manifest(;
    dataset_version::AbstractString,
    benchmark_id::AbstractString,
    system_config::AbstractDict,
    observation_config::AbstractDict,
    split_config::AbstractDict,
    window_configs::AbstractVector,
    task_configs::AbstractVector,
    benchmark_config::AbstractDict,
    generated_files::AbstractDict,
    diagnostics::AbstractDict,
)
    return Dict(
        "dataset_version" => String(dataset_version),
        "release_date" => string(today()),
        "benchmark_id" => String(benchmark_id),
        "system_id" => system_config["system_id"],
        "observation_id" => observation_config["observation_id"],
        "split_id" => split_config["split_id"],
        "window_ids" => [config["window_id"] for config in window_configs],
        "task_ids" => [config["task_id"] for config in task_configs],
        "difficulty_level" => benchmark_config["difficulty_level"],
        "release_version" => benchmark_config["release_version"],
        "solver_name" => system_config["solver_name"],
        "solver_abstol" => system_config["solver_abstol"],
        "solver_reltol" => system_config["solver_reltol"],
        "seed" => system_config["seed_policy"]["generation_seed"],
        "state_dim" => system_config["state_dim"],
        "eigenvalues" => system_config["default_parameters"]["eigenvalues"],
        "dt" => system_config["dt"],
        "trajectory_length" => system_config["trajectory_length"],
        "num_trajectories" => system_config["num_trajectories"],
        "generated_files" => generated_files,
        "diagnostics" => diagnostics,
        "notes" => "unit_internal linear diagonal smoke dataset generated by exact diagonal flow",
    )
end

## 2. Validate manifest completeness

function validate_manifest(manifest::AbstractDict)
    required_keys = [
        "dataset_version",
        "release_date",
        "benchmark_id",
        "system_id",
        "observation_id",
        "split_id",
        "window_ids",
        "task_ids",
        "difficulty_level",
        "solver_name",
        "seed",
        "state_dim",
        "eigenvalues",
        "dt",
        "trajectory_length",
        "num_trajectories",
        "generated_files",
        "diagnostics",
    ]

    for key in required_keys
        haskey(manifest, key) || throw(ArgumentError("manifest is missing key: $(key)"))
    end

    return true
end
